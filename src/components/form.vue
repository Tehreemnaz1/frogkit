<template>
  <form @submit.prevent="displayError">
    <slot></slot>
  </form>
</template>

<script>
  export default {
    name: 'Form',
    props: {
      value: {
        type: Boolean,
        default: false
      },
      showError: {
        type: Boolean,
        default: false
      }
    },
    watch:{
      showError() {
        this.displayError()
      }
    },
    data () {
      return {
        isValid: false,
        inputs: [],
        errors: {}
      }
    },
    mounted () {
      this.watchInputs()
    },
    methods: {
      watchInputs () {
        this.inputs = search(this.$children)
        this.inputs.forEach(input => {
          input.$watch('valid', this.validate)
        })
        this.validate()
      },
      validate() {
        this.isValid = this.inputs.every(input => input.valid)
        this.$emit('input', this.isValid)
      },
      displayError() {
        this.inputs.forEach(input => {
          input.touched = true
        })
      }
    }
  }

  function search (children = [], depth = 0, results = []) {
    children.forEach(child => {
      if (child.validatorElement !== undefined) {
        results.push(child)
      } else {
        search(child.$children, depth + 1, results)
      }
    })

    if (depth === 0) return results
  }
</script>
